<h2 class="post-title"><a href="#">State Profile - Kerala</a></h2>
</div>
<div class="detail-inner">
<p>Kerala is situated in the southwestern end of the Indian subcontinent. Kerala has a long history of art and cultural heritage and foreign trade with other countries. The state with the highest literacy rate in India, is noted for its achievements in education, health, gender equality, social justice, law and order. In addition to these, the state has the lowest infant mortality rate in the country.</p>

<p>Kerala lies between the Arabian Sea in the West and the Western Ghats (Sahyadris) in the East with an area of 38863 sq km. It is one of the five states in the linguistic-cultural area known as South India. The neighbouring states of Kerala are Tamil Nadu and Karnataka. Mayyazhi (Mahe / Mahe) is a part of Pondicherry (Puducherry) and lies within Kerala. Though the Lakshadweeps in the Arabian Sea is part of the Union Territories, it has a close alliance with the linguistic and cultural heritage of Kerala.</p>

<p>Before the independence of India, Kerala was one of the princely states in India. Later on 1 July 1949, the princely states of Travancore and Cochin united to form the Travancore-Cochin State. Later, When the Malabar region (formerly part of Madras state) was added to the Travancore-Cochin State. The state was formed on 1 November 1956.</p>

<h3>The Land</h3>

<p>The tropical climate and the rich monsoons offer beautiful landscapes, the presence of abundant water bodies, long beaches and more than 40 rivers add to the charm. Popular by the name "God's Own Country", the location of Kerala is geographically located between 74 0 7 '47 "and 770 37" 12 "north latitudes and  80 17 '30' and 12 0 47 east longitudes. </p>

<p>The most popular legend on the origin of Kerala says that Kerala rose up from the sea when Lord Parasurama threw his axe into the Sea from Gokarna.  Lord Parasurama, believed to be the sixth avatar of Lord Mahavishnu, threw his axe from Gokarna southward across the ocean in rage and in repentance for his actions of killing the Kshatriyas and the sea receded upto where his axe had fallen and brought up a narrow strip of land called Kerala.</p>

<p>Geographically Kerala is divided in east-west direction into three parts- Highland, Mid plains and coastal areas. The area in and around the Western Ghats or Sahyadri are mostly hilly and thick evergreen rainforests. The major rivers of Kerala originate from these highlands. Silent Valley in Palakkad district is one of the bio-diversity hotspots in the world. The highest peak in Kerala is Anamudi (2695 m).  On the West, the Coastal belt lies parallel to the Western Ghats. In between, the highland and coastal plain lies the mid-lands. It is generally a combination of hills and valleys. The 41 rivers flowing to the Arabian Sea and the lakes in the west, the three east flowing rivers, the lakes and backwaters make Kerala a   water-rich landmass.</p> 

<h3>Weather</h3>

<p>Kerala experiences a tropical climate, as it is normally located at a distance of 80 from the equator. There are three types of seasons in Kerala - June-September South-West monsoon (Edavappathy), October - December North-East monsoon  (Thula Varsham) and summer season(March- May). The winter season in Kerala during the months of December and February is not a marked one, compared to other parts of India.  The climate of Kerala is characterized by mild winds and rapid monsoon showers.  </p>


