<h3>Thiruvananthapuram</h3>
<p>Thiruvananthapuram, the jewel in the emerald necklace that Kerala is for the Indian sub-continent, must surely have been a must-see destination for ages, long before National Geographic Traveller classified it as one. Surely, long before Sage Parasurama, according to local legend, threw his divine battle axe from Kanyakumari to Gokarnam to wrest Kerala, 'God's Own Land' from Varuna the Sea God; before the times of mythical Mahabali the democratic and just ruler of this wonderful land who was sent down to the netherworld through deceit. It doesn't take any flights of fancy to imagine that this land fired the imaginations of intrepid travellers and explorers like Columbus, Vasco da Gama, Marco Polo, Fa Hien, and quite possibly, countless others from the pages of history, recorded or not.</p>

<h3>Kollam</h3>
<p>Kollam or Quilon, an old sea port town on the Arabian coast, stands on the Ashtamudi Lake. Kollam, the erstwhile Desinganadu, had a sustained commercial reputation from the days of the Phoenicians and the Romans. Fed by the Chinese trade, it was regarded by Ibn Batuta, as one of the five ports , which he had seen in the course of his travels during a period of twenty four years, in the 14th century. Kollam District which is a veritable Kerala in miniature is gifted with unique representative features - sea, lakes, plains, mountains, rivers, streams, backwaters, forest, vast green fields and tropical crop of every variety both food crop and cash crop.</p>

<h3>Alappuzha</h3>
<p>Alappuzha is a landmark between the broad Arabian Sea and a net work of rivers flowing into it. In the early first decade of the 20th Century the then Viceroy of the Indian Empire, Lord Curzon made a visit in the State to Alleppey, now Alappuzha. Fascinated by the Scenic beauty of the place, in joy and amazement, he said, Here nature has spent up on the land her richest bounties. In his exhilaration, it is said, he exclaimed, Alleppey, the Venice of the east.</p>

<p>Alappuzha is a landmark between the broad Arabian Sea and a net work of rivers flowing into it. In the early first decade of the 20th Century the then Viceroy of the Indian Empire, Lord Curzon made a visit in the State to Alleppey, now Alappuzha. Fascinated by the Scenic beauty of the place, in joy and amazement, he said, Here nature has spent up on the land her richest bounties. In his exhilaration, it is said, he exclaimed, Alleppey, the Venice of the East. Thus the sobriquet found its place in the world Tourism Map. The presence of a port and a pier, criss -cross roads and numerous bridges across them, a long and unbroken sea coast might have motivated him to make this comparison. Alleppey has a wonderful past. Though the present town owes its existence to the sagacious Diwan Rajakesavadas in the second half of 18th century, district of Alappuzha figures in classified Literature. Kuttanad, the rice bowl of Kerala with the unending stretch of paddy fields, small streams and canals with lush green coconut palms, was well known even from the early periods of the Sangam age. History says Alappuzha had trade relations with ancient Greece and Rome in B.C and in the Middle Ages.</p>

<h3>Pathanamthitta</h3>
<p>Pathanamthitta is a combination of two words Pathanam and Thitta, which mean an array of houses on the river side. This district was formed on 1st November 1982 in the interest of the hastening process of development. It is presumed that the regions presently under the district were formerly under the Pandalam reign which had connections with the Pandya Kingdom. Pathanamthitta now includes portions of the erstwhile Kollam Alappuzha and Idukki districts. Pathanamthitta, Adoor, Ranni, Konni and Kozhencherry are some of the important places taken from Kollam district, whereas Thiruvalla and Mallappally are the major places taken from Alappuzha district.</p>

<h3>Kottayam</h3>
<p>Kottayam is located in central Kerala and is also the administrative capital of Kottayam district. Bordered by the lofty and mighty Western Ghats on the east and the Vembanad Lake and paddy fields of Kuttanad on the west, Kottayam is a land of unique characteristics. Panoramic backwater stretches, lush paddy fields, highlands, hills and hillocks, extensive rubber plantations, places associated with many legends and a totally literate people have given Kottayam District the enviable title: The land of letters, legends, latex and lakes. The city is an important trading center of spices and commercial crops, especially rubber.</p>

<h3>Idukki</h3>
<p>This beautiful high range district of Kerala is geographically known for its hills and dense forests. For the people of Kerala, Idukki is always associated with power generation. About 66% of the State's power needs come from the Hydroelectric Power Projects in Idukki. The district accounts for 12.9 per cent of the area of Kerala and only 3.7 per cent of the population of Kerala. Idukki district was formed on 26 January 1972. The district consists of Devikulam, Udumbanchola and Peermedu taluks of the erstwhile Kottayam district and Thodupuzha taluk (excluding two villages Manjallore and Kalloorkadu) of the erstwhile Ernakulam district. At the time of formation the district headquarters started functioning at Kottayam and from there it was shifted to Painavu in Thodupuzha taluk in June 1976, where it is proposed to build a new planned forest township.</p>

<h3>Ernakulam</h3>
<p>Ernakulam District was formed on 1 April 1958, from the taluks of Aluva, Kunnathunadu, Kochi, Kanayannur, and Paravoor, which were formerly part of Thrissur District. Initially the district headquarters was at Ernakulam, which gave the district its name the headquarters was later shifted to Kakkanad. The word/name Ernakulam is derived from a Tamil word Erayanarkulam which means abode of Lord Shiva. Ernakulam district is situated almost at the middle of Kerala State and on the coast of the Arabian Sea. It is also the most industrially advanced and flourishing District of Kerala.</p>

<h3>Thrissur</h3>
<p>The history of Thrissur District from the 9th to the 12th centuries is the history of Kulasekharas of Mahodayapuram and the history since 12th century is the history of the rise and growth of Perumpadappu Swarupam. In the course of its long and chequered history, the Perumpadappu Swarupam had its capital at different places. We learn from the literary works of the period that the Perumpadappu Swarupam had its headquarters at Mahodayapuram and that a number of Naduvazhies in Southern and Central Kerala recognized the supremacy of the Perumpadappu Moopil. The Perumpadappu Moopil is even referred to as the "Kerala Chakravarthi" in the "Sivavilasam" and some other works. One of the landmarks in the history of the Perumpadapu Swarupam is the foundation of a new era called Pudu Vaipu Era. The Pudu Vaipu Era is traditionally believed to have commenced from the date on which the island of Vypeen was formed, following a massive flood.</p>

<h3>Palakkad</h3>
<p>Palakkad is one of the few districts in Kerala that has no coastline. The district opens the state to the rest of the country through the Palakkad Gap with a width of 32 to 40 km. Its geographical position, historical background, educational status, tourism hot-spots and above all, the development activities that are carried out, are wide and varied. The district is one of the main granaries of Kerala and its economy is primarily agricultural. The district is also the land of palmyrahs. The present Palakkad district, as an administrative unit, was formed on the first of January 1957, comprising Palakad, Perinthalmanna, Ponnani, Ottapalam, Alathur and Chittur. Later when Malappuram District was formed excluding Thritala firka of Ponnani Taluk, Mankada firka and excluding Karkidamkunnu & Chetalloor amsoms of Perinthelmanna Taluk are transferred to that district and a new taluk was formed namely Mannarkkad. In 2013, Ottapalam taluk was bifurcated and Pattambi taluk was formed.</p>

<h3>Malappuram</h3>
<p>Malappuram (literally, a land a tops hills) is situated 50 km southeast of Kozhikode, bounded by the Nilgiri Hills in the east, the Arabian sea in the west and Thrissur and Palakkad districts in the south. Malappuram is enriched by three great rivers flowing through it - the Chaliyar, the Kadalundi and the Bharathappuzha. Malappuram has a rich and eventful history. It was the military headquarters of the Zamorins of Kozhikode since ancient times. This district was the venue for many of the Mappila revolts (uprisings against the British East India Company in Kerala) between 1792 and 1921. It was a famous centre for Hindu - Vedic learning and Islamic philosophy and a place of cultural heritage.</p>

<h3>Kozhikode</h3>
<p>Opening up the gateway of India to Vasco-da-Gama the adventurous Portuguese navigator in 1498, Kozhikode has carved for itself a landmark in the history of India. The land of the ancient Zamorins had many more things to offer to the western world other than the savoury spices for which they even ventured to discover a sea route. Occupying a prominent place in the international trade map of the country right from the 13th century Kozhikode paved the way for trade tourism in India. This trade centre is regaining much of its ancient glory by opening up air routes to Persian Gulf and other regions.</p>

<h3>Wayanad</h3>
<p>Wayanad District came into existence on 1st November, 1980 as the 12th District of Kerala consisting of Mananthavady, Sulthan Bathery and Vythiri taluks. The name Wayanad is derived from Vayal Nadu which means the land of paddy fields. It is a picturesque plateau situated at a height between 700 m and 2100 m above the mean sea level nested among the mountains of the Western Ghats on the eastern portion of north Kerala, bordering the states of Tamil Nadu and Karnataka. The district was carved out from the then Kozhikode and Kannur districts. About 885.92 sq. km of area is under forest. The culture of Wayanad is mainly tribal oriented. Though considered as backward, this district is perhaps one of the biggest foreign exchange earners of the State, with its production of cash crops like pepper, cardamom, coffee, tea, spices and other condiments.</p>

<h3>Kannur</h3>
<p>Kannur district derived its name from the location of its headquarters at Kannur town. The old name 'Cannanore' is the anglicised form of the Malayalam word Kannur. According to one opinion, 'Kannur' is a derivation from Kanathur, an ancient village, the name of which survives even today in one of the wards of Kannur Municipality. Another version is that Kannur might have assumed its name from one of the, deities of the Hindu pantheon, a compound of two words, Kannan (Lord Krishna) and Ur (place) making it the place of Lord Krishna. In this context, it is worth mentioning that the deity of the Katalayi Sreekrishna temple was originally installed in a shrine at Katalayi Kotta in the south eastern part of the present Kannur town.</p>

<h3>Kasaragod</h3>
<p>Lying at the northern tip of Kerala bounded by the Western Ghats in the east and Arabian sea in the west; twelve rivers flowing across its terrain, Kasaragod is an enchanting beauty of Nature's creations. There are different views on the derivation of the name "KASARAGOD". One view is that it is the combination of two Sanskrit words Kaasaara (which means lake or pond) and Kroda (which means a place where treasure is kept). Another view is that it is the place where Kaasaraka trees (Strychnos nux vomica or Kaanjiram or Kaaraskara) are in abundance. Both views are relevant as there are large number of rivers, lakes and ponds in the coastal belt of the district besides thick flora consisting of innumerable varieties of trees, shrubs etc. particularly plenty of Kaasaraka trees. Many Arab travellers, who came to Kerala between 9th and 14th centuries A.D., visited Kasaragod as it was then an important trade centre. They called this area Harkwillia. Mr. Barbose, the Portuguese traveller,who visited Kumbla near Kasaragod in 1514, had recorded that rice was exported to Male Island whence coir was imported. Dr.Francis Buchanan, who was the family doctor of Lord Wellesly, visited Kasaragod in 1800. In his travelogue, he has included information on the political and communal set-up in places like Athiparamba, Kavvai, Nileshwar, Bekkal, Chandragiri and Manjeshwar.</p>

